#include <iostream>
using namespace std;

int main() {

    cout << "hello world in diferent languages" << endl;
    // use '\n' in string, and 'endl' as variable, to break lines

    cout << "ola mundo" << endl;
    cout << "hola mundo\n";
    // the lines above break one line

    cout << "مرحبا بالعالم" << endl << endl;
    cout << "Hello dinja\n\n";
    // the lines above break two lines, result in one blank line between

    cout << "that's all folks";

    return 0;
}